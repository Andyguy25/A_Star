vector<pair<city, double>> AStarHelper(const vector<pair<city, double>> adj[], double(&eucLookup)[LAST_ITEM][LAST_ITEM], vector<pair<city, double>>& visited, vector<pair<city, double>>& nextRecur, bool& finished, city curr, city end) {

	if (curr == end) {
		finished = true;
	}
	if (finished == true && nextRecur.size() == 0) {
		return visited;
	}

	//for loop through all the neighbors of a vector and add them into the nextrecur vector by its distance as a priority
	//call recursive function once you finish processing each vertex
	for (auto neighbors = adj[curr].begin(); neighbors != adj[curr].end(); neighbors++) {

		vector<pair<city, double>> tempRecur;
		//compare neighbor distance with visited array at that city to see if its faster
		if ((neighbors->second + visited[curr].second) < visited[neighbors->first].second) {

			//update visited info for that city
			visited[neighbors->first].first = curr;
			visited[neighbors->first].second = neighbors->second + visited[curr].second;

			bool found = false;
			for (auto checkList = nextRecur.begin(); checkList != nextRecur.end(); checkList++) {
				if (checkList->first == neighbors->first) {
					found = true;
				}
			}

			if (found == false) {

				bool beenPlaced = false;
				for (auto distCompare = nextRecur.begin(); distCompare != nextRecur.end(); distCompare++) {

					if ((neighbors->second + euclidean(eucLookup, neighbors->first, end)) < (distCompare->second + euclidean(eucLookup, distCompare->first, end)) && beenPlaced == false) {
						tempRecur.push_back(make_pair(neighbors->first, neighbors->second));
						beenPlaced = true;
						tempRecur.push_back(make_pair(distCompare->first, distCompare->second));
					}
					else {
						tempRecur.push_back(make_pair(distCompare->first, distCompare->second));
					}
				}

				if (beenPlaced == false) {
					tempRecur.push_back(make_pair(neighbors->first, neighbors->second));
					beenPlaced = true;
				}


				nextRecur.swap(tempRecur);
				tempRecur.clear();


			}
		}
	}

	//if queue is not empty, take next item from the front out and use it
	if (nextRecur.size() > 0) {
		city nextCity = nextRecur.begin()->first;

		vector<pair<city, double>>::iterator delFirstCity;
		delFirstCity = nextRecur.begin();
		nextRecur.erase(delFirstCity);

		AStarHelper(adj, eucLookup, visited, nextRecur, finished, nextCity, end);

	}

	return visited;
}



void DFS(const vector<pair<city, double>> adj[], city start, city end) {
	vector<bool> visited;
	vector<pair<city, double>> completed;

	//initialize the visited vector to be all be false, index is the city
	for (int i = 0; i < LAST_ITEM; i++) {
		visited.push_back(false);
	}

	completed = DFSHelper(adj, visited, completed, start, end);
	cout << "From " << cityToString(start) << " to " << cityToString(end) << " you would take:\n\n";
	cout << cityToString(start) << " to " << cityToString(completed.begin()->first) << " - " << completed.begin()->second << endl;
	for (auto cities = completed.begin() + 1; cities != completed.end(); cities++) {
		cout << cityToString((cities - 1)->first) << " to " << cityToString(cities->first) << " - " << cities->second << endl;
	}
}

vector<pair<city, double>> DFSHelper(const vector<pair<city, double>> adj[], vector<bool>& visited, vector<pair<city, double>>& completed, city curr, city end) {

	visited[curr] = true;

	if (curr == end) {
		return completed;
	}

	//go through all the cities neighboring cities
	for (auto cityWithWeight = adj[curr].begin(); cityWithWeight != adj[curr].end(); cityWithWeight++) {

		//should only be called if city ISNT visited, visited is false
		if (visited[cityWithWeight->first] == false) {
			DFSHelper(adj, visited, completed, cityWithWeight->first, end);
		}

		//if the cities been found, add city its on with weight to vector and return
		if (visited[end] == true) {
			completed.insert(completed.begin(), (make_pair(cityWithWeight->first, cityWithWeight->second)));
			return completed;
		}

	}
	return completed;
}


